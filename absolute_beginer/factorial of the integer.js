// You are provided with a number, "N". Find its factorial.

// Input Description:
// A positive integer is provided as an input.

// Output Description:
// Print the factorial of the integer.

// Sample Input :
// 2
// Sample Output :
// 2

var a=10;
var temp=1;
for(var i=1;i<=a;i++){
    temp *=i;
}
